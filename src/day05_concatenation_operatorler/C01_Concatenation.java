package day05_concatenation_operatorler;

public class C01_Concatenation {
    public static void main(String[] args) {

        System.out.println("25" + "34");// 2534

        System.out.println("java" + 2 + 7);// "java2" + 7 ==> java27

        System.out.println(2 + 7 + "java");// 9 + " java " ==> 9java

        System.out.println("java" + 2 * 7);//"java" + 14 ==> java14

        System.out.println("" + 5 + 3);//"5"+3==> 53

    }
}
